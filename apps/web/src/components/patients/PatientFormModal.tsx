import React, { useState, useEffect, useCallback } from 'react';
import { Patient } from '../../types/patient';
import { PatientStatus } from '../../api/generated/xEarCRMAPIAutoGenerated.schemas';
import { validateTcNumber } from '../../utils/tcValidator';
import { 
  Input,
  Select,
  Textarea,
  Button,
  DatePicker,
  Modal
} from '@x-ear/ui-web';

interface PatientFormData {
  firstName?: string;
  lastName?: string;
  phone: string;
  tcNumber?: string;
  birthDate?: string;
  email?: string;
  address?: string;
  status?: PatientStatus;
  segment?: string;
  acquisitionType?: string;
  branchId?: string;
  tags?: string[];
}

interface PatientFormModalProps {
  isOpen: boolean;
  onClose: () => void;
  onSubmit: (data: Partial<Patient>) => Promise<Patient | null>;
  initialData?: Patient | null;
  title?: string;
  className?: string;
}

/**
 * PatientFormModal Component
 * Modal form for creating and editing patients
 * Follows 500 LOC limit and single responsibility principle
 */
export function PatientFormModal({
  isOpen,
  onClose,
  onSubmit,
  initialData,
  title,
  className = ''
}: PatientFormModalProps): JSX.Element | null {
  const [formData, setFormData] = useState<PatientFormData>({
    firstName: '',
    lastName: '',
    phone: '',
    tcNumber: '',
    birthDate: '',
    email: '',
    address: '',
    status: 'ACTIVE',
    segment: 'NEW',
    acquisitionType: 'tabela',
    tags: []
  });

  const [errors, setErrors] = useState<Record<string, string>>({});
  const [isSubmitting, setIsSubmitting] = useState(false);

  // Initialize form data
  useEffect(() => {
    if (initialData) {
      setFormData({
        firstName: initialData.firstName || '',
        lastName: initialData.lastName || '',
        phone: initialData.phone || '',
        tcNumber: initialData.tcNumber || '',
        birthDate: initialData.birthDate || '',
        email: initialData.email || '',
        address: typeof initialData.address === 'string' ? initialData.address : initialData.addressFull || '',
        status: initialData.status || 'ACTIVE',
        segment: initialData.segment || '',
        acquisitionType: initialData.acquisitionType || '',
        branchId: initialData.branchId || '',
        tags: initialData.tags || []
      });
    } else {
      // Reset form for new patient
      setFormData({
        firstName: '',
        lastName: '',
        phone: '',
        tcNumber: '',
        birthDate: '',
        email: '',
        address: '',
        status: 'ACTIVE',
        segment: 'NEW',
        acquisitionType: 'tabela',
        branchId: '',
        tags: []
      });
    }
    setErrors({});
  }, [initialData, isOpen]);

  const validateForm = useCallback((): boolean => {
    const newErrors: Record<string, string> = {};

    // Required fields
    if (!formData.firstName?.trim()) {
      newErrors.firstName = 'Ad gereklidir';
    }

    if (!formData.lastName?.trim()) {
      newErrors.lastName = 'Soyad gereklidir';
    }

    if (!formData.phone?.trim()) {
      newErrors.phone = 'Telefon numarası gereklidir';
    } else if (!/^[\d\s\-()+]+$/.test(formData.phone)) {
      newErrors.phone = 'Geçerli bir telefon numarası giriniz';
    }

    // TC Number validation (optional)
    if (formData.tcNumber) {
      const tcValidation = validateTcNumber(formData.tcNumber);
      if (!tcValidation.isValid) {
        newErrors.tcNumber = tcValidation.errorMessage;
      }
    }

    // Email validation (optional)
    if (formData.email?.trim()) {
      const email = formData.email.trim();
      if (!email.includes('@')) {
        newErrors.email = 'E-posta adresi @ içermelidir';
      } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
        newErrors.email = 'Geçerli bir e-posta adresi giriniz (\u00f6rn: ornek@email.com)';
      }
    }

    // Birth date validation (optional)
    if (formData.birthDate) {
      const birthDate = new Date(formData.birthDate);
      const today = new Date();
      if (birthDate > today) {
        newErrors.birthDate = 'Doğum tarihi bugünden sonra olamaz';
      }
    }

    setErrors(newErrors);
    return Object.keys(newErrors || {}).length === 0;
  }, [formData]);

  const handleInputChange = useCallback((field: keyof PatientFormData, value: string) => {
    setFormData(prev => ({
      ...prev,
      [field]: value
    }));
    
    // Clear error when user starts typing
    if (errors[field]) {
      setErrors(prev => ({
        ...prev,
        [field]: ''
      }));
    }
  }, [errors]);

  const handleSelectEventChange = useCallback((field: keyof PatientFormData, event: React.ChangeEvent<HTMLSelectElement>) => {
    setFormData(prev => ({
      ...prev,
      [field]: event.target.value
    }));
    
    // Clear error when user starts typing
    if (errors[field]) {
      setErrors(prev => ({
        ...prev,
        [field]: ''
      }));
    }
  }, [errors]);

  const handleSubmit = useCallback(async (e: React.FormEvent) => {
    e.preventDefault();
    
    if (!validateForm()) {
      return;
    }

    setIsSubmitting(true);
    // Clear previous errors
    setErrors({});
    
    try {
      // Clean formData: convert empty strings to undefined/null for optional fields
      const cleanedData = {
        ...formData,
        tcNumber: formData.tcNumber?.trim() || undefined,
        email: formData.email?.trim() || undefined,
        birthDate: formData.birthDate?.trim() || undefined,
        address: formData.address?.trim() || undefined,
        branchId: formData.branchId?.trim() || undefined,
        acquisitionType: formData.acquisitionType?.trim() || undefined,
      };
      
      const result = await onSubmit(cleanedData);
      if (result) {
        onClose();
      }
    } catch (error: any) {
      console.error('Form submission error:', error);
      
      // Check error structure comprehensively
      const status = error?.response?.status || error?.status;
      const errorData = error?.response?.data || error?.data;
      const errorMsg = errorData?.error || errorData?.message || error?.message;
      
      console.log('Error status:', status);
      console.log('Error message:', errorMsg);
      console.log('Full error:', JSON.stringify(error, null, 2));
      
      // Backend validation errors (400)
      if (status === 400) {
        if (errorMsg && typeof errorMsg === 'string') {
          if (errorMsg.toLowerCase().includes('telefon') || errorMsg.toLowerCase().includes('phone')) {
            setErrors({ phone: errorMsg });
          } else if (errorMsg.toLowerCase().includes('tc') || errorMsg.toLowerCase().includes('kimlik')) {
            setErrors({ tcNumber: errorMsg });
          } else if (errorMsg.toLowerCase().includes('email') || errorMsg.toLowerCase().includes('e-posta')) {
            setErrors({ email: errorMsg });
          } else {
            setErrors({ submit: errorMsg });
          }
        } else {
          setErrors({ submit: 'Validasyon hatası' });
        }
        return;
      }
      
      // Backend duplicate errors (409 Conflict)
      if (status === 409) {
        if (errorMsg && typeof errorMsg === 'string') {
          if (errorMsg.toLowerCase().includes('tc') || errorMsg.toLowerCase().includes('kimlik')) {
            setErrors({ tcNumber: 'Bu TC Kimlik numarası zaten kayıtlı' });
          } else if (errorMsg.toLowerCase().includes('telefon') || errorMsg.toLowerCase().includes('phone')) {
            setErrors({ phone: 'Bu telefon numarası zaten kayıtlı' });
          } else if (errorMsg.toLowerCase().includes('email') || errorMsg.toLowerCase().includes('e-posta')) {
            setErrors({ email: 'Bu e-posta adresi zaten kayıtlı' });
          } else {
            setErrors({ submit: errorMsg });
          }
        } else {
          setErrors({ submit: 'Bu kayıt zaten mevcut' });
        }
        return;
      }
      
      // Generic error message
      if (errorMsg && typeof errorMsg === 'string') {
        // Try to match field-specific errors
        if (errorMsg.toLowerCase().includes('telefon') || errorMsg.toLowerCase().includes('phone')) {
          setErrors({ phone: errorMsg });
        } else if (errorMsg.toLowerCase().includes('tc') || errorMsg.toLowerCase().includes('kimlik')) {
          setErrors({ tcNumber: errorMsg });
        } else if (errorMsg.toLowerCase().includes('email') || errorMsg.toLowerCase().includes('e-posta')) {
          setErrors({ email: errorMsg });
        } else {
          setErrors({ submit: errorMsg });
        }
      } else {
        setErrors({ submit: 'Bir hata oluştu. Lütfen tekrar deneyin.' });
      }
    } finally {
      setIsSubmitting(false);
    }
  }, [formData, validateForm, onSubmit, onClose]);

  const handleClose = useCallback(() => {
    if (!isSubmitting) {
      onClose();
    }
  }, [isSubmitting, onClose]);

  return (
    <Modal
      isOpen={isOpen}
      onClose={handleClose}
      title={title}
      size="xl"
      showFooter={false}
      className={className}
    >
      <form onSubmit={handleSubmit} className="space-y-6">
            {/* Personal Information */}
            <div>
              <h3 className="text-sm font-semibold text-gray-900 mb-3">Kişisel Bilgiler</h3>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Ad <span className="text-red-500">*</span>
                  </label>
                <Input
                  type="text"
                  value={formData.firstName}
                  onChange={(e) => handleInputChange('firstName', e.target.value)}
                  placeholder="Hasta adı"
                />
                {errors.firstName && (
                  <p className="mt-1 text-sm text-red-600">{errors.firstName}</p>
                )}
              </div>

                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Soyad <span className="text-red-500">*</span>
                  </label>
                  <Input
                    type="text"
                    value={formData.lastName}
                    onChange={(e) => handleInputChange('lastName', e.target.value)}
                    placeholder="Hasta soyadı"
                  />
                  {errors.lastName && (
                    <p className="mt-1 text-sm text-red-600">{errors.lastName}</p>
                  )}
                </div>
              </div>
            </div>

            {/* Contact Information */}
            <div>
              <h3 className="text-sm font-semibold text-gray-900 mb-3">İletişim Bilgileri</h3>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Telefon <span className="text-red-500">*</span>
                  </label>
                  <Input
                    type="text"
                    value={formData.phone}
                    onChange={(e) => handleInputChange('phone', e.target.value)}
                    placeholder="0555 123 45 67"
                  />
                  {errors.phone ? (
                    <p className="mt-1 text-sm text-red-600">{errors.phone}</p>
                  ) : (
                    <p className="mt-1 text-xs text-gray-500">Örnek: 0555 123 45 67</p>
                  )}
              </div>

                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    E-posta
                  </label>
                  <Input
                    type="text"
                    value={formData.email}
                    onChange={(e) => handleInputChange('email', e.target.value)}
                    placeholder="ornek@email.com"
                  />
                  {errors.email ? (
                    <p className="mt-1 text-sm text-red-600">{errors.email}</p>
                  ) : (
                    <p className="mt-1 text-xs text-gray-500">İsteğe bağlı</p>
                  )}
                </div>
              </div>
            </div>

            {/* Identity Information */}
            <div>
              <h3 className="text-sm font-semibold text-gray-900 mb-3">Kimlik Bilgileri</h3>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    TC Kimlik No
                  </label>
                  <Input
                    type="text"
                    value={formData.tcNumber}
                    onChange={(e) => handleInputChange('tcNumber', e.target.value)}
                    placeholder="12345678901"
                    maxLength={11}
                  />
                  {errors.tcNumber ? (
                    <p className="mt-1 text-sm text-red-600">{errors.tcNumber}</p>
                  ) : (
                    <p className="mt-1 text-xs text-gray-500">11 haneli TC kimlik numarası</p>
                  )}
              </div>

                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-1">
                    Doğum Tarihi
                  </label>
                  <DatePicker
                    value={formData.birthDate ? new Date(formData.birthDate) : null}
                    onChange={(date) => handleInputChange('birthDate', date ? date.toISOString().split('T')[0] : '')}
                    placeholder="Doğum tarihi seçin"
                    error={errors.birthDate}
                    fullWidth
                  />
                  {!errors.birthDate && (
                    <p className="mt-1 text-xs text-gray-500">İsteğe bağlı</p>
                  )}
                </div>
              </div>
            </div>

            {/* Address */}
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-1">
                Adres
              </label>
              <Textarea
                value={formData.address}
                onChange={(e) => handleInputChange('address', e.target.value)}
                rows={4}
                placeholder="Hasta adresi"
                className="resize-none"
              />
            </div>

            {/* Classification */}
            <div>
              <h3 className="text-sm font-semibold text-gray-900 mb-3">Sınıflandırma</h3>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">
                  Durum
                </label>
                <Select
                  value={formData.status}
                  onChange={(event) => handleSelectEventChange('status', event)}
                  options={[
                    { value: 'ACTIVE', label: 'Aktif' },
                    { value: 'INACTIVE', label: 'Pasif' }
                  ]}
                />
              </div>

              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">
                  Segment
                </label>
                <Select
                  value={formData.segment}
                  onChange={(event) => handleSelectEventChange('segment', event)}
                  options={[
                    { value: 'NEW', label: 'Yeni' },
                    { value: 'TRIAL', label: 'Deneme' },
                    { value: 'PURCHASED', label: 'Satın Almış' },
                    { value: 'CONTROL', label: 'Kontrol' },
                    { value: 'RENEWAL', label: 'Yenileme' },
                    { value: 'EXISTING', label: 'Mevcut' },
                    { value: 'VIP', label: 'VIP' }
                  ]}
                />
                </div>
              </div>
            </div>

            <div>
              <h3 className="text-sm font-semibold text-gray-900 mb-3">Kazanım Bilgileri</h3>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">
                  Kazanım Türü
                </label>
                <Select
                  value={formData.acquisitionType}
                  onChange={(event) => handleSelectEventChange('acquisitionType', event)}
                  options={[
                    { value: 'referral', label: 'Referans' },
                    { value: 'online', label: 'Online' },
                    { value: 'walk-in', label: 'Ziyaret' },
                    { value: 'social-media', label: 'Sosyal Medya' },
                    { value: 'advertisement', label: 'Reklam' },
                    { value: 'tabela', label: 'Tabela' },
                    { value: 'other', label: 'Diğer' }
                  ]}
                />
              </div>

              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">
                  Şube
                </label>
                <Select
                  value={formData.branchId}
                  onChange={(event) => handleSelectEventChange('branchId', event)}
                  options={[
                    { value: '', label: 'Seçiniz' },
                    { value: 'branch-1', label: 'Merkez Şube' },
                    { value: 'branch-2', label: 'Kadıköy Şube' },
                    { value: 'branch-3', label: 'Beşiktaş Şube' }
                  ]}
                />
                </div>
              </div>
            </div>

            {/* Submit Error */}
            {errors.submit && (
              <div className="p-3 bg-red-50 border border-red-200 rounded-lg">
                <p className="text-sm text-red-600">{errors.submit}</p>
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center justify-end gap-3 pt-6 border-t border-gray-200">
              <Button
                type="button"
                onClick={handleClose}
                disabled={isSubmitting}
                variant="ghost"
              >
                İptal
              </Button>
              <Button
                type="submit"
                disabled={isSubmitting}
                variant="primary"
              >
                {isSubmitting ? 'Kaydediliyor...' : (initialData ? 'Güncelle' : 'Kaydet')}
              </Button>
            </div>
          </form>
    </Modal>
  );
}