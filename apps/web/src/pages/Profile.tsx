import React, { useState } from 'react';
import { useAuthStore } from '../stores/authStore';
import { Button, Input, Card, CardHeader, CardTitle, CardContent } from '@x-ear/ui-web';
import { User, Mail, Shield, Key, Save } from 'lucide-react';
import { toast } from 'react-hot-toast';
import { useUsersUpdateMe, useUsersChangePassword } from '../api/generated/xEarCRMAPIAutoGenerated';

export const ProfilePage: React.FC = () => {
    const { user, setUser } = useAuthStore();

    // Form states
    const [name, setName] = useState(user?.name || '');
    const [currentPassword, setCurrentPassword] = useState('');
    const [newPassword, setNewPassword] = useState('');
    const [confirmPassword, setConfirmPassword] = useState('');

    const updateMeMutation = useUsersUpdateMe({
        mutation: {
            onSuccess: (data) => {
                toast.success('Profil bilgileri güncellendi');
                // Update local user store if needed
                if (data.data) {
                    // Assuming data.data is the User object. 
                    // Need to map it correctly if auth store expects specific structure.
                    // For now, just updating name in store if possible or relying on re-fetch.
                    // setUser({ ...user, name: data.data.first_name + ' ' + data.data.last_name });
                }
            },
            onError: () => {
                toast.error('Güncelleme başarısız');
            }
        }
    });

    const changePasswordMutation = useUsersChangePassword({
        mutation: {
            onSuccess: () => {
                toast.success('Şifre başarıyla değiştirildi');
                setCurrentPassword('');
                setNewPassword('');
                setConfirmPassword('');
            },
            onError: (error: any) => {
                toast.error(error.response?.data?.error || 'Şifre değiştirilemedi');
            }
        }
    });

    const handleUpdateProfile = (e: React.FormEvent) => {
        e.preventDefault();
        // Split name into first and last name for API
        const parts = name.trim().split(' ');
        const firstName = parts[0];
        const lastName = parts.slice(1).join(' ') || '';

        updateMeMutation.mutate({
            data: {
                firstName,
                lastName
            }
        });
    };

    const handleUpdatePassword = (e: React.FormEvent) => {
        e.preventDefault();

        if (newPassword !== confirmPassword) {
            toast.error('Yeni şifreler eşleşmiyor');
            return;
        }

        changePasswordMutation.mutate({
            data: {
                currentPassword,
                newPassword
            }
        });
    };

    return (
        <div className="space-y-6 max-w-4xl mx-auto">
            <div>
                <h1 className="text-2xl font-bold text-gray-900 dark:text-white">Profil Ayarları</h1>
                <p className="text-gray-500 dark:text-gray-400">Kişisel bilgilerinizi ve hesap güvenliğinizi yönetin.</p>
            </div>

            <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
                {/* Basic Info Card */}
                <Card>
                    <CardHeader>
                        <CardTitle className="flex items-center gap-2">
                            <User className="w-5 h-5" />
                            Kişisel Bilgiler
                        </CardTitle>
                    </CardHeader>
                    <CardContent>
                        <form onSubmit={handleUpdateProfile} className="space-y-4">
                            <div className="space-y-2">
                                <label className="text-sm font-medium text-gray-700 dark:text-gray-300">
                                    Ad Soyad
                                </label>
                                <div className="relative">
                                    <User className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-gray-400" />
                                    <Input
                                        value={name}
                                        onChange={(e) => setName(e.target.value)}
                                        className="pl-10"
                                        placeholder="Adınız Soyadınız"
                                    />
                                </div>
                            </div>

                            <div className="space-y-2">
                                <label className="text-sm font-medium text-gray-700 dark:text-gray-300">
                                    E-posta Adresi
                                </label>
                                <div className="relative">
                                    <Mail className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-gray-400" />
                                    <Input
                                        value={user?.email || ''}
                                        disabled
                                        className="pl-10 bg-gray-50 dark:bg-gray-800"
                                    />
                                </div>
                                <p className="text-xs text-gray-500">E-posta adresi değiştirilemez.</p>
                            </div>

                            <div className="space-y-2">
                                <label className="text-sm font-medium text-gray-700 dark:text-gray-300">
                                    Rol
                                </label>
                                <div className="relative">
                                    <Shield className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-gray-400" />
                                    <Input
                                        value={user?.role || 'Kullanıcı'}
                                        disabled
                                        className="pl-10 bg-gray-50 dark:bg-gray-800 capitalize"
                                    />
                                </div>
                            </div>

                            <div className="pt-4">
                                <Button type="submit" disabled={updateMeMutation.isPending} className="w-full">
                                    <Save className="w-4 h-4 mr-2" />
                                    {updateMeMutation.isPending ? 'Kaydediliyor...' : 'Değişiklikleri Kaydet'}
                                </Button>
                            </div>
                        </form>
                    </CardContent>
                </Card>

                {/* Password Change Card */}
                <Card>
                    <CardHeader>
                        <CardTitle className="flex items-center gap-2">
                            <Key className="w-5 h-5" />
                            Şifre Değiştir
                        </CardTitle>
                    </CardHeader>
                    <CardContent>
                        <form onSubmit={handleUpdatePassword} className="space-y-4">
                            <div className="space-y-2">
                                <label className="text-sm font-medium text-gray-700 dark:text-gray-300">
                                    Mevcut Şifre
                                </label>
                                <Input
                                    type="password"
                                    value={currentPassword}
                                    onChange={(e) => setCurrentPassword(e.target.value)}
                                    placeholder="••••••••"
                                />
                            </div>

                            <div className="space-y-2">
                                <label className="text-sm font-medium text-gray-700 dark:text-gray-300">
                                    Yeni Şifre
                                </label>
                                <Input
                                    type="password"
                                    value={newPassword}
                                    onChange={(e) => setNewPassword(e.target.value)}
                                    placeholder="••••••••"
                                />
                            </div>

                            <div className="space-y-2">
                                <label className="text-sm font-medium text-gray-700 dark:text-gray-300">
                                    Yeni Şifre (Tekrar)
                                </label>
                                <Input
                                    type="password"
                                    value={confirmPassword}
                                    onChange={(e) => setConfirmPassword(e.target.value)}
                                    placeholder="••••••••"
                                />
                            </div>

                            <div className="pt-4">
                                <Button type="submit" variant="outline" disabled={changePasswordMutation.isPending} className="w-full">
                                    {changePasswordMutation.isPending ? 'Güncelleniyor...' : 'Şifreyi Güncelle'}
                                </Button>
                            </div>
                        </form>
                    </CardContent>
                </Card>
            </div>
        </div>
    );
};

export default ProfilePage;
