import { useState, useEffect } from 'react';
import { inventoryGetInventoryItems } from '../api/generated/xEarCRMAPIAutoGenerated';
import type { InventoryItem } from '../api/generated/schemas';

interface UseInventoryResult {
  products: InventoryItem[];
  loading: boolean;
  error: string | null;
  searchProducts: (query: string) => InventoryItem[];
  refetch: () => Promise<void>;
}

export const useInventory = (): UseInventoryResult => {
  const [products, setProducts] = useState<InventoryItem[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  const fetchInventory = async () => {
    try {
      setLoading(true);
      setError(null);

      const response = await inventoryGetInventoryItems({
        category: 'hearing_aid', // Focus on hearing aids for sales
        search: undefined,
        lowStock: false
      });

      // Handle different response structures
      const responseData = response.data as any;
      if (responseData?.data?.items) {
        setProducts(responseData.data.items as InventoryItem[]);
      } else if (responseData?.items) {
        setProducts(responseData.items as InventoryItem[]);
      } else if (Array.isArray(responseData?.data)) {
        setProducts(responseData.data as InventoryItem[]);
      } else if (Array.isArray(responseData)) {
        setProducts(responseData as InventoryItem[]);
      } else {
        // Fallback or empty
        setProducts([]);
      }
    } catch (err) {
      console.error('Error fetching inventory:', err);
      setError(err instanceof Error ? err.message : 'Failed to fetch inventory');
      setProducts([]);
    } finally {
      setLoading(false);
    }
  };

  const searchProducts = (query: string): InventoryItem[] => {
    if (!query.trim()) return products;

    const searchTerm = query.toLowerCase();
    return products.filter(product =>
      product.name?.toLowerCase().includes(searchTerm) ||
      product.brand?.toLowerCase().includes(searchTerm) ||
      product.model?.toLowerCase().includes(searchTerm) ||
      product.barcode?.toLowerCase().includes(searchTerm)
    );
  };

  const refetch = async () => {
    await fetchInventory();
  };

  useEffect(() => {
    fetchInventory();
  }, []);

  return {
    products,
    loading,
    error,
    searchProducts,
    refetch
  };
};