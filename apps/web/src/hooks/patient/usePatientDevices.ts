import { useState, useEffect, useCallback } from 'react';
import { PatientDevice } from '../../types/patient';
import { patientsGetPatientDevices } from '../../api/generated/xEarCRMAPIAutoGenerated';

interface ApiResponse {
  success?: boolean;
  data?: PatientDevice[];
  meta?: Record<string, unknown>;
}

/**
 * Hook for managing patient devices
 * Fetches devices from API and provides refetch capability
 */
export function usePatientDevices(patientId: string) {
  const [devices, setDevices] = useState<PatientDevice[]>([]);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<Error | string | null>(null);

  const fetchDevices = useCallback(async () => {
    if (!patientId) {
      setDevices([]);
      return;
    }

    try {
      setLoading(true);
      setError(null);

      const response = await patientsGetPatientDevices(patientId);
      const responseData = response as unknown as ApiResponse;

      // API response structure: { success: boolean, data: PatientDevice[], meta: {...} }
      if (responseData?.success && Array.isArray(responseData.data)) {
        console.log('ðŸ“¥ Patient devices from backend:', responseData.data);
        const firstDevice = responseData.data[0] as Record<string, unknown> | undefined;
        console.log('ðŸ“¥ First device FULL:', JSON.stringify(firstDevice, null, 2));
        console.log('ðŸ“¥ Serial numbers check:', {
          serialNumber: firstDevice?.serialNumber,
          serial_number: firstDevice?.serial_number,
          serialNumberLeft: firstDevice?.serialNumberLeft,
          serial_number_left: firstDevice?.serial_number_left,
          serialNumberRight: firstDevice?.serialNumberRight,
          serial_number_right: firstDevice?.serial_number_right
        });
        setDevices(responseData.data);
      } else {
        console.warn('Unexpected API response structure:', responseData);
        setDevices([]);
      }
    } catch (err) {
      console.error('Failed to fetch patient devices:', err);
      setError(err instanceof Error ? err : new Error('Failed to fetch patient devices'));
      setDevices([]);
    } finally {
      setLoading(false);
    }
  }, [patientId]);

  // Fetch devices when patientId changes
  useEffect(() => {
    fetchDevices();
  }, [patientId]); // Only depend on patientId, not fetchDevices

  // Listen for device updates
  useEffect(() => {
    const handleDeviceUpdate = () => {
      fetchDevices();
    };

    window.addEventListener('device:assigned', handleDeviceUpdate);
    window.addEventListener('device:updated', handleDeviceUpdate);
    window.addEventListener('device:removed', handleDeviceUpdate);

    return () => {
      window.removeEventListener('device:assigned', handleDeviceUpdate);
      window.removeEventListener('device:updated', handleDeviceUpdate);
      window.removeEventListener('device:removed', handleDeviceUpdate);
    };
  }, [patientId]); // Only depend on patientId, not fetchDevices

  // Clear error
  const clearError = useCallback(() => {
    setError(null);
  }, []);

  return {
    // Data
    devices,
    data: devices,

    // State
    loading,
    isLoading: loading,
    error,

    // Actions
    refetch: fetchDevices,
    clearError
  };
}