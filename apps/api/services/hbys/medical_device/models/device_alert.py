"""
Device Alert Model
Tracks alerts and warnings generated by or about medical devices.
"""
from sqlalchemy import Column, String, Text, DateTime, Boolean, ForeignKey
from sqlalchemy.orm import relationship

from core.models.base import BaseModel
from core.models.mixins import TenantScopedMixin
from database import Base, gen_id, now_utc


class DeviceAlert(Base, TenantScopedMixin, BaseModel):
    __tablename__ = "medical_device_alerts"

    id = Column(String(36), primary_key=True, default=lambda: gen_id("dal"))
    device_id = Column(
        String(36),
        ForeignKey("medical_device_registry.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )

    # Alert classification
    alert_type = Column(String(30), nullable=False)
    # alert_type: offline / error / calibration_due / warranty_expiry / communication_failure / data_error
    severity = Column(String(10), nullable=False, default="info")
    # severity: info / warning / critical

    # Alert content
    message = Column(Text, nullable=False)

    # Resolution tracking
    resolved = Column(Boolean, nullable=False, default=False)
    resolved_by = Column(String(36), nullable=True)
    resolved_at = Column(DateTime, nullable=True)

    # Relationships
    device = relationship("DeviceRegistry", back_populates="alerts")

    def to_dict(self):
        result = self.to_dict_base()
        result.update({
            "id": self.id,
            "deviceId": self.device_id,
            "alertType": self.alert_type,
            "severity": self.severity,
            "message": self.message,
            "resolved": self.resolved,
            "resolvedBy": self.resolved_by,
            "resolvedAt": self._format_datetime_utc(self.resolved_at),
            "tenantId": self.tenant_id,
        })
        return result
